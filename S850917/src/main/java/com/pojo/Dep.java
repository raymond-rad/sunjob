package com.pojo;

public class Dep {
    protected int DepId;
    protected String DepName;

    public void setDepId(int depId) {
        DepId = depId;
    }

    public void setDepName(String depName) {
        DepName = depName;
    }

    public int getDepId() {
        return DepId;
    }

    public String getDepName() {
        return DepName;
    }

}
